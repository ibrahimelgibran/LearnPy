# operator input perhitungan
a = int(input("inputnya nilai a: "))
b = int(input("inputnya nilai b: "))

# perjumlahan pertambahan
c = a + b
print("Hasil %d + %d = %d" % (a,b,c))

# perjumblahan pengurangan
c = a - b
print("Hasil %d - %d = %d" % (a,b,c))

# perjumblahan perkalian
c = a * b
print("Hasil %d * %d = %d" % (a,b,c))

# perjumblahan pembagaian
c = a / b
print("Hasil %d / %d = %d" % (a,b,c))
print("Hasil %d / %d =" % (a,b),c)
print("Hasil %d / %d = %f" % (a,b,c))
print("Hasil %d / %d = %s" % (a,b,c))

# operator sisa bagi
c = a % b
print("Hasil %d %% %d = %d" % (a.b.c)) #hasil dari int diatas

# operator pangkat
c = a ** b
print("Hasil %d ** %d = %d" % (a,b,c))  #hasil dari int diatas
